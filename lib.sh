#!/bin/bash

# ──────────────────────────────────────────────
# Colors
# ──────────────────────────────────────────────
BOLD="\033[1m"
DIM="\033[2m"
GREEN="\033[32m"
YELLOW="\033[33m"
CYAN="\033[36m"
RESET="\033[0m"

# ──────────────────────────────────────────────
# Helpers
# ──────────────────────────────────────────────
ok()   { printf "  ${GREEN}✓${RESET} ${DIM}$1${RESET}\n"; }
skip() { printf "  ${DIM}↩ $1 — skipped${RESET}\n"; }
info() { printf "  ${DIM}$1${RESET}\n"; }
